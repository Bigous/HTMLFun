import { EventEmitter, Injectable } from '@angular/core';

@Injectable()
export class Levels {
  private levels = levels;
  private currentLevel = 0;

  public onLevelChanged: EventEmitter<number> = new EventEmitter<number>();

  constructor() {}

  get CurrentLevel() {
    return this.currentLevel;
  }
  set CurrentLevel(level: number) {
    if (this.levels.length < level && level >= 0) {
      this.currentLevel = level;
      this.onLevelChanged.emit(this.currentLevel);
    }
  }

  get Level(): Array<string> {
    return this.levels[this.currentLevel];
  }

  public toToNextLevel() {
    this.CurrentLevel++;
  }

  public toToPriorLevel() {
    this.CurrentLevel--;
  }

  get IsFirstLevel(): boolean {
    return this.currentLevel === 0;
  }

  get IsLastLevel(): boolean {
    return this.currentLevel === this.levels.length - 1;
  }
}

